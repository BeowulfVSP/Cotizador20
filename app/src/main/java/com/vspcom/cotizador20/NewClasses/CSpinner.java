package com.vspcom.cotizador20.NewClasses;

public class CSpinner {
    private String id;
    private String descripcion;

    public CSpinner(String id, String descripcion) {
        this.id = id;
        this.descripcion = descripcion;
    }

    public String getId() {
        return id;
    }

    public String getDescripcion() {
        return descripcion;
    }
}
